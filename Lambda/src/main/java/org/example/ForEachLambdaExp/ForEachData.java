package org.example.ForEachLambdaExp;

public class ForEachData {
    String name;

    public ForEachData(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    @Override
    public String toString() {
        return "ForEachData{" +
                "name='" + name + '\'' +
                '}';
    }
}
